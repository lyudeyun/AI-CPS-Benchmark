% String Utility Toolbox 
% Version 1.5   18-Jul-2002 
% 
% 
% brackets      - Find brackets and parts of string enclosed in brackets. 
% ddeblank      - Double deblank. Removes both leading and trailing blanks of string.
% ddewhite      - Double dewhite. Strip both leading and trailing whitespace.
% dewhite       - Dewhite. Strip trailing whitespace.
% dm2unix       - Convert DOS and MAC line endings of string to UNIX line endings.
% extract       - Extract subentries of a VECTOR into a LIST (matrix or cell)
% findname      - Finding name (word) within a string.
% findnl        - Find new line characters and line numbers
% findquot      - Find quote and comment mask as well as new lines and line numbers.
% getnames      - Finds all possible legal names in a string.
% hex2bin       - Convert hexadecimal string to a binary string.
% hex2int16     - Convert hexadecimal string to int16 type.
% hex2int32     - Convert hexadecimal string to int32 type.
% hex2int8      - Convert hexadecimal string to int8 type.
% hex2uint16    - Convert hexadecimal string to uint16 type.
% hex2uint32    - Convert hexadecimal string to uint32 type.
% hex2uint8     - Convert hexadecimal string to uint8 type.
% insert        - Inserts entries from LIST into a VECTOR
% lexsort       - Lexical sorting of a character array 
% munique       - Extraction of unique rows out of a character array
% rot13         - Rot13 (Caesar) encryption.
% rstrrep       - Recursive string replacement.
% sizestr       - Return size of array as a string.
% str2cell      - Convert a string to a cell array of lines.
% str2lines     - Convert a string into a list of lines.
% strcenter     - Center each line of text.
% strflushleft  - Flush left each line of text.
% strflushright - Flush right each line of text.
% strim         - Removes the leading and trailing character of a string.
% strjoin       - Join strings in a cell array.
% strlexcmp     - Lexicographic comparison of two strings.
% strltok       - Find last token in string.
% stros         - Return the name of the OS under which the text was created.
% strrepl       - Replaces the string S2 in S1 with S3.
% strwcmp       - Compares two strings allowing wildcards.
% substr        - Extract a substring out of a string.
% unrot13       - Rot13 (Caesar) decryption.
% untabify      - Convert tabs to spaces.
% varname       - Return the name of the variable as a string
% vectoriz      - Vectorize expression or inline function object.
% wordcase      - Captializes first letters of words 
% combinestructs - Combines 2 structures
% int2str        - Convert integer to string (Fast version).
% num2str2       - Convert number to string. (Fast version)
% parsemfilestr  - Returns function names used in m-file string.
% strpat         - EXACT pattern search and replacement
