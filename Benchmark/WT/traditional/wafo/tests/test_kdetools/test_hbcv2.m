function test_hbcv2_()
   % data = rndnorm(0, 1,5,2); 
  data = [-0.0233845632050972   0.9070186193622006;... 
           0.6529594866766634   1.3689145060433903;... 
           0.4477857310723146  -0.6311953712037597;... 
          -1.9256785038579962   0.5886257667993168;... 
          -0.5290011931824666  -0.3602090880229930]; 
  tol = 1e-7;
  assert(hbcv2(data,'gaus'), [0.482681614203333 1.133242297848955], tol);
  assert(hbcv2(data,'epan'), [1.05495723163671 2.55257402255233], tol);
  assert(hbcv2(data,'biwe'), [1.24293653366899 2.98949541257312], tol);
  assert(hbcv2(data,'triw'), [1.43099550062034, 3.39546227138374], tol);
  assert(hbcv2(data,'tria'), [ 1.15255694491467, 2.76211028087912], tol);
  assert(hbcv2(data,'rect'), [0.837614274875370, 1.997573853072259], tol);
  assert(hbcv2(data,'lapl'), [0.362634943481836, 0.873248947994519], tol);
  assert(hbcv2(data,'logi'), [ 0.266350664349379, 0.652154211531075], tol);
end
